			<aside class="col3">I			<h2>Socrates</h2>
			<p>It is useful to have examined the number of meanings of a term both for clearness' sake (for a man is more likely to know what it is he asserts, if it bas been made clear to him how many meanings it may have), and also with a view to ensuring that our reasonings shall be in accordance with the actual facts and not addressed merely to the term used. </p>
				
			<p>For as long as it is not clear in how many senses a term is used, it is possible that the answerer and the questioner are not directing their minds upon the same thing: whereas when once it has been made clear how many meanings there are, and also upon which of them the former directs his mind when he makes his assertion, the questioner would then look ridiculous if he failed to address his argument to this.</p>
			<h3>What is he talking about?</h3>
			<p>It helps us to both avoid being misled and to mislead by false reasoning: for if we know the number of meanings of a term, we shall certainly never be misled by false reasoning, but shall certainly never be misled by false reasoning, but shall know if the reasoner fails to address his argument to the same point; and when we ourselves put the questions we shall be able to mislead him, if our answerer happens not to know the number of meanings of our terms.</p>
			<p>This, however is not possible in all cases, but only when of the many senses some are true and others are false. This manner of argument, however, does not belong properly to dialectic; dialectitians should therefore by all means beware of this kind of verbal discussion, unless one is absolutely unable to discuss the subject before him in any way.</p>
			<h3>Commenting on his sentence</h3>
			<p>Let us reflect in another way, and we shall see that there is great reason to hope that death is a good, for one of two things: - either death is a state of nothingness and utter unconsciousness, or, as men say, there is a change and migration of the soul from this world to another. Now if you suppose that there is no consciousness, but a sleep like the sleep of him who is undisturbed even by the sight of dreams, death will be an unspeakable gain. For if a person were to select the night in which his sleep was undisturbed even by dreams, and were to compare with this the other days and nights of his life, and then were to tell us how many days and nights he had passed in the course of his life better and more pleasantly than this one, I think that any man, I will not say a private man, but even the great king, will not find many such days or nights, when compared with the others. Now if death is like this, I say that to die is gain; for eternity is then only a single night.</p>
			<p>But if death is the journey to another place, and there, as men say, all the dead are, what good, O my friends and judges, can be greater than this? If indeed when the pilgrim arrives in the world below, he is delivered from the professors of justice in this world, and finds the true judges who are said to give judgment there, Minos and Rhadamanthus and Aeacus and Triptolemus, and other sons of God who were righteous in their own life, that pilgrimage will be worth making. What would not a man give if he might converse with Orpheus and Musaeus and Hesiod and Homer?</p>
			
			<p>Nay, if this be true, let me die again and again. I, too, shall have a wonderful interest in a place where I can converse with Palamedes, and Ajax the son of Telamon, and other heroes of old, who have suffered death through an unjust judgment; and there will be no small pleasure, as I think, in comparing my own sufferings with theirs. Above all, I shall be able to continue my search into true and false knowledge; as in this world, so also in that; I shall find out who is wise, and who pretends to be wise, and is not. What would not a man give, O judges, to be able to examine the leader of the great Trojan expedition; or Odysseus or Sisyphus, or numberless others, men and women too! What infinite delight would there be in conversing with them and asking them questions! For in that world they do not put a man to death for this; certainly not. For besides being happier in that world than in this, they will be immortal, if what is said is true.</p>
			
			<p>Wherefore, O judges, be of good cheer about death, and know this of a truth - that no evil can happen to a good man, either in life or after death. He and his are not neglected by the gods; nor has my own approaching end happened by mere chance. But I see clearly that to die and be released was better for me; and therefore the oracle gave no sign. For which reason also, I am not angry with my accusers, or my condemners; they have done me no harm, although neither of them meant to do me any good; and for this I may gently blame them.</p></aside>
